def contains_arabic(text):
    if not text: return False
    return bool(re.search(r'[\u0600-\u06FF]', text))

def escape_po_string(text):
    if not text: return ""
    return text.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')

