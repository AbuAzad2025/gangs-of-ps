def translate_arabic_word(tok):
    direct = AUTO_DIRECT_TRANSLATIONS.get(tok) or AUTO_WORD_TRANSLATIONS.get(tok) or TRANSLATIONS.get(tok)
    if direct is not None:
        return direct

    prefix_map = {
        "لل": "to",
        "و": "and",
        "ف": "then",
        "ب": "with",
        "ل": "to",
        "ك": "like",
        "ال": "",
    }
    for pref, pref_tr in prefix_map.items():
        if tok.startswith(pref) and len(tok) > len(pref):
            base = tok[len(pref):]
            base_tr = AUTO_DIRECT_TRANSLATIONS.get(base) or AUTO_WORD_TRANSLATIONS.get(base) or TRANSLATIONS.get(base)
            if base_tr is None and base.startswith("ال") and len(base) > 2:
                base2 = base[2:]
                base_tr = AUTO_DIRECT_TRANSLATIONS.get(base2) or AUTO_WORD_TRANSLATIONS.get(base2) or TRANSLATIONS.get(base2)
            if base_tr is None:
                return None
            if pref_tr == "":
                return base_tr
            return f"{pref_tr} {base_tr}".strip()
    return None

def split_arabic_with_punct(token):
    m = re.match(r'^([\u0600-\u06FF]+)([^\u0600-\u06FF]+)$', token)
    if m:
        return m.group(1), m.group(2)
    m = re.match(r'^([^\u0600-\u06FF]+)([\u0600-\u06FF]+)$', token)
    if m:
        return m.group(2), m.group(1)
    return None, None

def auto_translate(msgid):
    if not msgid:
        return None
    if msgid in AUTO_DIRECT_TRANSLATIONS:
        return AUTO_DIRECT_TRANSLATIONS[msgid]
    if not contains_arabic(msgid):
        return msgid

    if msgid in TRANSLATIONS:
        return TRANSLATIONS[msgid]

    tokens = re.findall(r'%\([^)]+\)[sd]|{\w+}|[A-Za-z0-9_./+\-]+|[\u0600-\u06FF]+|\S', msgid)
    if not tokens:
        return None
    out = []
    for t in tokens:
        suffix = ""
        prefix = ""
        if contains_arabic(t):
            while t and t[-1] in "،؛؟:,.!?":
                suffix = t[-1] + suffix
                t = t[:-1]
            while t and t[0] in "،؛؟:,.!?":
                prefix = prefix + t[0]
                t = t[1:]
        mapped = None
        if t in AUTO_DIRECT_TRANSLATIONS:
            mapped = AUTO_DIRECT_TRANSLATIONS[t]
        elif t in AUTO_WORD_TRANSLATIONS:
            mapped = AUTO_WORD_TRANSLATIONS[t]
        elif t in TRANSLATIONS:
            mapped = TRANSLATIONS[t]
        elif re.fullmatch(r'[\u0600-\u06FF]+', t):
            mapped = translate_arabic_word(t)
        elif contains_arabic(t):
            core, punct = split_arabic_with_punct(t)
            if core:
                core_tr = translate_arabic_word(core) or AUTO_WORD_TRANSLATIONS.get(core) or AUTO_DIRECT_TRANSLATIONS.get(core) or TRANSLATIONS.get(core)
                if core_tr is not None:
                    mapped = f"{core_tr}{punct}"
        if mapped is not None:
            out.append(f"{prefix}{mapped}{suffix}")
        else:
            out.append(f"{prefix}{t}{suffix}")

    s = " ".join(out)
    s = re.sub(r'\s+([,:;.!?\)\]\}\u060C\u061B\u061F])', r'\1', s)
    s = re.sub(r'([\(\[\{])\s+', r'\1', s)
    s = re.sub(r'\s{2,}', ' ', s).strip()
    s = s.replace('،', ',').replace('؟', '?').replace('؛', ';')
    if contains_arabic(s):
        s = transliterate_arabic(s)
    if s == "":
        return None
    return s

def load_po_translations(path):
    if not os.path.exists(path):
        return {}
    with open(path, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    result = {}
    i = 0
    while i < len(lines):
        line = lines[i]
        if line.strip().startswith('msgid'):
            current_msgid = ""
            val = line.strip()[5:].strip()
            if val.startswith('"') and val.endswith('"'):
                current_msgid += val[1:-1]
            j = i + 1
            while j < len(lines) and lines[j].strip().startswith('"'):
                val = lines[j].strip()
                if val.startswith('"') and val.endswith('"'):
                    current_msgid += val[1:-1]
                j += 1
            clean_msgid = current_msgid.replace('\\"', '"').replace('\\n', '\n')
            current_msgstr = ""
            if j < len(lines) and lines[j].strip().startswith('msgstr'):
                val = lines[j].strip()[6:].strip()
                if val.startswith('"') and val.endswith('"'):
                    current_msgstr += val[1:-1]
                j += 1
                while j < len(lines) and lines[j].strip().startswith('"'):
                    val = lines[j].strip()
                    if val.startswith('"') and val.endswith('"'):
                        current_msgstr += val[1:-1]
                    j += 1
            clean_msgstr = current_msgstr.replace('\\"', '"').replace('\\n', '\n')
            if clean_msgid and clean_msgstr:
                result[clean_msgid] = clean_msgstr
            i = j
        else:
            i += 1
    return result

def load_python_translations_dict(script_path):
    if not os.path.exists(script_path):
        return {}
    try:
        src = open(script_path, 'r', encoding='utf-8').read()
        tree = ast.parse(src, filename=script_path)
        for node in tree.body:
            if isinstance(node, ast.Assign):
                for target in node.targets:
                    if isinstance(target, ast.Name) and target.id == "translations":
                        val = ast.literal_eval(node.value)
                        if isinstance(val, dict):
                            return {k: v for k, v in val.items() if isinstance(k, str) and isinstance(v, str)}
        return {}
    except Exception:
        return {}

def update_translations():
    if not os.path.exists(PO_FILE):
        print(f"Error: {PO_FILE} not found.")
        return

    us_translations = load_po_translations(EN_US_PO_FILE)
    recovered_translations = load_po_translations(RECOVERED_PO_FILE)
    extra_translations = load_python_translations_dict(os.path.join(os.path.dirname(__file__), "update_translations.py"))

    print(f"Reading {PO_FILE}...")
    with open(PO_FILE, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    new_lines = []
    i = 0
    updated_count = 0

    while i < len(lines):
        line = lines[i]
        
        if line.strip().startswith('msgid'):
            current_msgid = ""
            val = line.strip()[5:].strip()
            if val.startswith('"') and val.endswith('"'):
                current_msgid += val[1:-1]
            j = i + 1
            while j < len(lines) and lines[j].strip().startswith('"'):
                val = lines[j].strip()
                if val.startswith('"') and val.endswith('"'):
                    current_msgid += val[1:-1]
                j += 1
            clean_msgid = current_msgid.replace('\\"', '"').replace('\\n', '\n')
            translation = TRANSLATIONS.get(clean_msgid)
            if translation is None and recovered_translations:
                translation = recovered_translations.get(clean_msgid)
            if translation is None and extra_translations:
                translation = extra_translations.get(clean_msgid)
            if translation is None and us_translations:
                translation = us_translations.get(clean_msgid)
            if translation is None:
                translation = auto_translate(clean_msgid)
            new_lines.append(line)
            k = i + 1
            while k < j:
                new_lines.append(lines[k])
                k += 1
            if j < len(lines) and lines[j].strip().startswith('msgstr'):
                current_msgstr = ""
                val = lines[j].strip()[6:].strip()
                if val.startswith('"') and val.endswith('"'):
                    current_msgstr += val[1:-1]
                j2 = j + 1
                while j2 < len(lines) and lines[j2].strip().startswith('"'):
                    val2 = lines[j2].strip()
                    if val2.startswith('"') and val2.endswith('"'):
                        current_msgstr += val2[1:-1]
                    j2 += 1
                clean_msgstr = current_msgstr.replace('\\"', '"').replace('\\n', '\n')
                should_update = False
                if translation is not None:
                    if clean_msgstr == "":
                        should_update = True
                    elif contains_arabic(clean_msgid) and contains_arabic(clean_msgstr) and not contains_arabic(translation):
                        should_update = True
                if should_update:
                    new_lines.append(f'msgstr "{escape_po_string(translation)}"\n')
                    updated_count += 1
                    print(f"Updated: {clean_msgid[:30]}...")
                else:
                    new_lines.append(lines[j])
                    j_keep = j + 1
                    while j_keep < j2:
                        new_lines.append(lines[j_keep])
                        j_keep += 1
                j = j2
            i = j
        else:
            new_lines.append(line)
            i += 1

    with open(PO_FILE, 'w', encoding='utf-8') as f:
        f.writelines(new_lines)
    print(f"Done. Updated {updated_count} empty translations.")

